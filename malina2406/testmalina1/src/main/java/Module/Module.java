package Module;

public class Module {

    int RF_id;
    String Name;
    String Type;

    public int getRF_id() {
        return RF_id;
    }

    public void setRF_id(int RF_id) {
        this.RF_id = RF_id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getType() {
        return Type;
    }

    public void setType(String type) {
        Type = type;
    }




}
